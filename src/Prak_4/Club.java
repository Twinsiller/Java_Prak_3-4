package Prak_4;

public class Club
{
    private int score = 0;
    private String name;

    Club (String name)
    {
        this.name = name;
    }

    public int getScore() {
        return score;
    }

    public void setScore(int score) {
        this.score = score;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}



